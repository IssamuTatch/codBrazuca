'''
Atividade Prática:
Escreva um programa que peça dois números ao usuário e exiba a soma, subtração,
multiplicação e divisão deles
'''

a = input('Digite um número: ')
b = input('Digite outro número: ')

print('Soma: ', float(a)+float(b))
print('Subtração: ', float(a)-float(b))
print('Multiplicação: ', float(a)*float(b))
print('Divisão: ', float(a)/float(b))